<p>
	Untuk menambahkan menu, pilih System->Menu Manager, kemudian klik new.
</p>
<p>
	Isi form:<br />
	- Nama, akan ditampilkan di link menu.<br />
	- Judul, akan ditampilkan di header bagian atas.<br />
	- Tipe, Left Menu akan ditampilkan di Menu Kiri, Top Menu akan ditampilkan di Menu Atas, Sub Menu untuk membuat link sepert 'New' & 'List'.<br />
	- Parent ID, Pilih parent/node dari menu yang akan dibuat.<br />
	- Tampil, 'Ya' maka menu akan ditampilkan, 'Tidak' maka menu tidak akan ditampilkan.<br />
	- Level Akses, pilih level user yang diizinkan mengakses menu tersebut.<br />
	- Aksi, pilih 'None'.
	- Letak File, masukkan letak file php-nya. (Letakkan file tsb di folder 'menus).<br />
	- Urutan, menu akan ditampilan berdasarkan urutan.
</p>